public class DatModle {

    String type, wiPrice, iPrice, wPrice;

    public DatModle() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWiPrice() {
        return wiPrice;
    }

    public void setWiPrice(String wiPrice) {
        this.wiPrice = wiPrice;
    }

    public String getiPrice() {
        return iPrice;
    }

    public void setiPrice(String iPrice) {
        this.iPrice = iPrice;
    }

    public String getwPrice() {
        return wPrice;
    }

    public void setwPrice(String wPrice) {
        this.wPrice = wPrice;
    }
}
